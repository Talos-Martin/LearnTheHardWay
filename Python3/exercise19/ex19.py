def cheese_and_crackheads(cheese_count, crackers):
    print(f"You have {cheese_count} cheese and {crackers} crackheads")
    print("Party!\n")

print("We can start here!")
cheese_and_crackheads(20,30)

print("OR we do it here:")
arg1 = 10
arg2 = 20
cheese_and_crackheads(arg1, arg2)

print("We can even do maths!")
cheese_and_crackheads(1+2, 2+3)

print("Or we do both")
cheese_and_crackheads(arg1+10, arg2+20)


